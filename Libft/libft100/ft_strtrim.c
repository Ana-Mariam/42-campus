#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	len;

	i = 0;
	if (!s1 || !set)
		return (NULL);
	len = ft_strlen(s1);
	while (s1[i] && ft_strchr(set, s1[i]) != (NULL))
		i++;
	while (s1[i] && ft_strchr(set, s1[len - 1]) != (NULL))
		len--;
	return (ft_substr(s1, i, (len - i)));
}
